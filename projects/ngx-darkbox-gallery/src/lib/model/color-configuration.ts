export interface ColorConfiguration {
  /**
   * A string representing a css color definition such as #FF00FF or rgba(0, 0, 0, 0.8)
   * Used to define the background color of the specific object
   */
  backgroundColor: string;

  /**
   * A string representing a css color definition such as #FF00FF or rgba(0, 0, 0, 0.8)
   * Used to define the foreground color, like text, of the specific object
   */
  foregroundColor: string;
}
